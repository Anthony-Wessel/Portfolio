  <p style="color:#AAA">This website not only is my portfolio, but is part of it. I created this website using several tools, as described before, in order to gain experience with web development and show off my abilities.</p>

  <br>

  <h3 style="color:#DDD">HTML and PHP</h3>
  <p style="color:#AAA">I used a number of tools to create this website. I used the Atom IDE to write and test my HTML and PHP scripts. I used a plugin to run a local php server to test the changes in real time. I also used the Bootstrap framework to format the website more cleanly.</p>

  <br>

  <h3 style="color:#DDD">JSON</h3>
  <p style="color:#AAA">I used JSON files to store the content for each article, so that I could use a single PHP file to dynamically load the content. I chose to do this so that I wouldn't have to create an individul PHP file for each article.</p>

  <br>

  <h3 style="color:#DDD">Github</h3>
  <p style="color:#AAA">I'm using Github to backup my project online and keep track of different versions.</p>

  <br>

  <h3 style="color:#DDD">AWS</h3>
  <p style="color:#AAA">I'm using AWS Lightsail to host the site. I also set up AWS CodePipeline and AWS CodeDeploy to update the Lightsail server with any new changes whenever I push my project to Github.</p>

